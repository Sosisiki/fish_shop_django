class Command(BaseCommand):
    help = "Заполняет каталог демо-данными (15 товаров в каждом из 4 разделов)"

    def handle(self, *args, **kwargs):
        count = 0
        for category, items in CATALOG_DATA.items():
            for name, desc, price, diff, vol, stock in items:
                Product.objects.update_or_create(
                    name=name,
                    defaults={
                        "description": desc,
                        "price": price,
                        "category": category,
                        "difficulty": diff,
                        "min_volume": vol,
                        "stock": stock,
                        "is_secret": False
                    }
                )
                count += 1
        self.stdout.write(self.style.SUCCESS(f"✅ Загружено/обновлено {count} товаров по 4 разделам."))