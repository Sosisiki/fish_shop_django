class Command(BaseCommand):
    help = "Добавление подробных описаний для всех товаров"

    def handle(self, *args, **options):
        success = 0
        skipped = 0
        errors = 0
        
        self.stdout.write(self.style.SUCCESS('\n✍️  ДОБАВЛЕНИЕ ОПИСАНИЙ ДЛЯ ТОВАРОВ\n'))
        
        for product in Product.objects.all():
            try:
                # Ищем описание по точному названию
                description = PRODUCT_DESCRIPTIONS.get(product.name)
                
                if not description:
                    # Поиск по ключевым словам
                    for key, desc in PRODUCT_DESCRIPTIONS.items():
                        if key.lower() in product.name.lower():
                            description = desc
                            break
                
                if description:
                    # Обновляем только если поле пустое
                    if not product.full_description:
                        product.full_description = description
                        product.save(update_fields=['full_description'])
                        self.stdout.write(
                            self.style.SUCCESS(f'✅ "{product.name}"')
                        )
                        success += 1
                    else:
                        self.stdout.write(
                            self.style.WARNING(f'⏭️  "{product.name}" — уже есть описание')
                        )
                        skipped += 1
                else:
                    self.stdout.write(
                        self.style.WARNING(f'⚠️  "{product.name}" — нет описания в базе')
                    )
                    errors += 1
                    
            except Exception as e:
                self.stdout.write(
                    self.style.ERROR(f'❌ "{product.name}": {str(e)[:50]}')
                )
                errors += 1
        
        self.stdout.write('\n' + '='*70)
        self.stdout.write(self.style.SUCCESS('📊 ИТОГИ:'))
        self.stdout.write(f'   ✅ Добавлено: {success}')
        self.stdout.write(f'   ⏭️  Пропущено: {skipped}')
        self.stdout.write(f'   ⚠️  Не найдено: {errors}')
        self.stdout.write('='*70)